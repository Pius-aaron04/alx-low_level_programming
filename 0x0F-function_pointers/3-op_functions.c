#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the difference of two numbers
 * @a: first number
 * @b: second number
 * Return: diff
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divdes a by c
 * @a: first number
 * @b: second number
 * Return: quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiplies a and b
 * @a: first number
 * @b: second number
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - finds modulos of a by b
 * @a: first number
 * @b: second number
 * Return: modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
